    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, selecione um habitat na tabela para excluir.", "Nenhuma Seleção", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int modelRow = jTable1.convertRowIndexToModel(selectedRow);
        int codigo = (int) jTable1.getModel().getValueAt(modelRow, 0);
        String nome = jTable1.getModel().getValueAt(modelRow, 1).toString();

        int confirm = JOptionPane.showConfirmDialog(this, 
                "Você tem certeza que deseja excluir o habitat '" + nome + "' (Código: " + codigo + ")?", 
                "Confirmar Exclusão", 
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            gestor.removerHabitat(codigo);
            JOptionPane.showMessageDialog(this, "Habitat excluído com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            atualizarTabela();
            limparCampos();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

