    private void configurarTabela(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        sorter = new TableRowSorter<>(model);
        jTable1.setRowSorter(sorter);
        
        jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        jTable1.getSelectionModel().addListSelectionListener(e -> {
            if(!e.getValueIsAdjusting()) {
                preencherCamposComLinhaSelecionada();
            }
        });
    }
        
    private void atualizarTabela() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Limpa a tabela

        List<Habitat> habitats = gestor.getHabitats();
        for (Habitat h : habitats) {
            Object[] rowData = {
                h.getCodigoHabitat(),
                h.getNome(), // Assumindo que o nome existe no modelo Habitat
                h.getBioma(),
                h.getArea(),
                h.getStatusLimpeza() ? "Limpo" : "Sujo" // Converte boolean para texto
            };
            model.addRow(rowData);
        }
    }
    
    private void limparCampos() {
        jTextField1.setText(""); // Codigo
        jTextField3.setText(""); // Nome
        jTextField2.setText(""); // Bioma
        jTextField4.setText(""); // Area
        buttonGroup1.clearSelection(); // Limpa a seleção do status (Limpo/Sujo)
        jTable1.clearSelection(); // Remove a seleção da tabela
    }
    
    private void preencherCamposComLinhaSelecionada() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            // Converte o índice da linha na view para o índice no modelo (caso esteja filtrado)
            int modelRow = jTable1.convertRowIndexToModel(selectedRow);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            jTextField1.setText(model.getValueAt(modelRow, 0).toString()); // Codigo
            jTextField3.setText(model.getValueAt(modelRow, 1).toString()); // Nome
            jTextField2.setText(model.getValueAt(modelRow, 2).toString()); // Bioma
            jTextField4.setText(model.getValueAt(modelRow, 3).toString()); // Area

            String status = model.getValueAt(modelRow, 4).toString();
            if (status.equals("Limpo")) {
                jRadioButton2.setSelected(true);
            } else {
                jRadioButton1.setSelected(true);
            }
        }
    }
    
    private void configurarBusca() {
        jTextField5.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) { filtrar(); }
            @Override
            public void removeUpdate(DocumentEvent e) { filtrar(); }
            @Override
            public void changedUpdate(DocumentEvent e) { filtrar(); }
        });
    }
    
    private void filtrar() {
        String texto = jTextField5.getText();
        if (texto.trim().length() == 0) {
            sorter.setRowFilter(null);
        } else {
            // Filtra por Código (coluna 0) OU Nome (coluna 1), ignorando maiúsculas/minúsculas
            RowFilter<Object, Object> rf = RowFilter.regexFilter("(?i)" + texto, 0, 1);
            sorter.setRowFilter(rf);
        }
    }
   
    
