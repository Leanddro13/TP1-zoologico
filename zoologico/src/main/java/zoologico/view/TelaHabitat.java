    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            int codigo = Integer.parseInt(jTextField1.getText());
            String nome = jTextField3.getText();
            String bioma = jTextField2.getText();
            double area = Double.parseDouble(jTextField4.getText());
            boolean statusLimpeza = jRadioButton2.isSelected(); // True se "Limpo" estiver selecionado

            if (nome.isEmpty() || bioma.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nome e Bioma são campos obrigatórios.", "Erro de Validação", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // A classe Habitat precisa de um construtor apropriado
            Habitat novoHabitat = new Habitat(codigo, nome, bioma, area, statusLimpeza);
            gestor.adicionarHabitat(novoHabitat);
            
            JOptionPane.showMessageDialog(this, "Habitat criado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            
            atualizarTabela();
            limparCampos();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Código e Área devem ser números válidos.", "Erro de Formato", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, selecione um habitat na tabela para editar.", "Nenhuma Seleção", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            int codigo = Integer.parseInt(jTextField1.getText());
            String nome = jTextField3.getText();
            String bioma = jTextField2.getText();
            double area = Double.parseDouble(jTextField4.getText());
            boolean statusLimpeza = jRadioButton2.isSelected();

            if (nome.isEmpty() || bioma.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nome e Bioma são campos obrigatórios.", "Erro de Validação", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            gestor.editarHabitat(codigo, nome, bioma, area, statusLimpeza);
            
            JOptionPane.showMessageDialog(this, "Habitat editado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

            atualizarTabela();
            limparCampos();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Código e Área devem ser números válidos.", "Erro de Formato", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed
    

    
